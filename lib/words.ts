function randRange(min: number, max: number) {
    min = Math.ceil(min);
    max = Math.floor(max);
    return Math.floor(Math.random() * (max - min) + min);
}

export function generateWordSets(length = 3): string[] {
    const selectedWords = [];
    for (let i = 0; i < length; i++) {
        const selRand = randRange(0, WORDBANK.length);
        selectedWords.push(WORDBANK[selRand]);
    }
    return selectedWords;
}
